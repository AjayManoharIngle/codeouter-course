package strings;

public class StringMethod8_LowerAndUpperCase {

	public static void main(String[] args) {
		
		//Lower case -> returns the string in lowercase letter.
		String name = "AJaY";
		System.out.println("Lower case : " + name.toLowerCase());
		
		// Upper case -> returns the string in uppercase letter.
		String name1 = "ajay";
		System.out.println("Upper case : " + name1.toUpperCase());
	}

}
